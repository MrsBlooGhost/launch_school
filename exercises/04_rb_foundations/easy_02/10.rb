# As before, we need to know when to stop iterating. This is easy if you recognize that the previous exercise had `n == 2` and we stopped processing when `index + 1`, or `index + 2 - 1` was greater than or equal to the array size. This translates to `index + n - 1` is greater than or equal to the array size.
# We pass a variable number of elements to the block using the splat operator together with a slice of the array that covers `n` elements starting at the current index.

def each_cons(array, n)
  array.each_index do |index|
    break if index + n - 1 >= array.size
    yield(*array[index..(index + n - 1)])
  end
  nil
end

hash = {}
each_cons([1, 3, 6, 10], 1) do |value|
  hash[value] = true
end
